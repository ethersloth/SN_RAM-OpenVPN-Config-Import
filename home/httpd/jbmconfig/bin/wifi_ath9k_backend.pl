sub read_jbm_conf_file
{
    my ( $config_filename ) = @_;
    if( not defined $config_filename )
    {
        print "ERROR: read_jbm_conf_file called without argument for config file name\n";
        return;
    }

    if( ! -e "$config_filename" )
    {
        print "ERROR: couldn't find config file: $config_filename\n";
        return;
    }

    print "\nReading config from: $config_filename\n\n"
        if( 0 < $DEBUG_LEVEL );

    my %config_data = ();
    $config_data_ref = \%config_data;
    %config_data = &read_config( "$config_filename" );

    if( 0 < $DEBUG_LEVEL )
    {
        for my $key ( keys %{$config_data_ref} )
        {
            print "$key = $config_data_ref->{$key}\n";
        }
    }
    return $config_data_ref if( defined $config_data_ref );
    return;
}

# Returns 1 if a variable is set to yes, y, on, true, or 1
sub is_option_enabled()
{
   my $option = $_[0];
   return 0 if not $option;
   return 1 if ( $option =~ /^yes$/i ||
                 $option =~ /^y$/i  ||
                 $option =~ /^on$/i ||
                 $option =~ /^true$/i ||
                 $option =~ /^enabled?$/i ||
                 1 eq $option );
   return 0;
}

# Returns 1 if a variable is st to no, n, off, false, or 0
sub is_option_disabled()
{
   my $option = $_[0];
   return 0 if not $option;
   return 1 if ( $option =~ /^no$/i ||
                 $option =~ /^n$/i  ||
                 $option =~ /^off$/i ||
                 $option =~ /^false$/i ||
                 $option =~ /^disabled?$/i ||
                 0 eq $option );
   return 0;
}

sub generate_hostapd_conf
{
    my ( $config_hash_ref ) = @_;
    if( ! defined $config_hash_ref )
    {
        print "ERROR: generate_hostapd_conf called without argument for config hash\n";
        return;
    }
       
    my $hostapd_conf = "";

    my $ssid = $config_hash_ref->{'SSID'};
    my $presharedkey = $config_hash_ref->{'PRESHAREDKEY'};
    my $encryption_mode = $config_hash_ref->{'ENCRYPTION'};
    my $ssid = $config_hash_ref->{'SSID'};
    my $channel = $config_hash_ref->{'CHANNEL'};

    if ( int($channel) < 1 || int($channel) > 14 )
    {
       $channel = 1;
    }

    #For now, always enable all bands
    my $band = $config_hash_ref->{'BAND'};
    
    
    my $ssid_broadcast_bits;
    if( &is_option_disabled( $config_hash_ref->{'SSIDBROADCAST'}) )
    {
        # 1 is empty SSID field
        # 2 is nulled SSID field (but may be needed by some clients?)
        $ssid_broadcast_bits = 2;
    }
    else
    {
        $ssid_broadcast_bits = 0;
    }

    my $wpa_bits = 0;

    # TODO: Turn on/off wpa/wpa2 as configured
    # 0 = off, 1 = wpa1, 2 = wpa2, 3 = wpa + wpa2
    #if( $encryption_mode ... )
    if ( "wpa" eq $encryption_mode ) 
    {
        $wpa_bits = 1;
    }
    elsif ( "wpa2" eq $encryption_mode ) 
    {
        $wpa_bits = 2;
    }
    elsif( "wpawpa2" eq $encryption_mode ) 
    {
        $wpa_bits = 3;
    }

    $hostapd_conf .= "# Warning: This file automatically generated by the GAU backend!\n";
    $hostapd_conf .= "interface=$wifi_int\n";
    $hostapd_conf .= "bridge=br0\n";
    $hostapd_conf .= "driver=nl80211\n";
    $hostapd_conf .= "ssid=$ssid\n";


    #Note: Band A is not supported by the ath9k_htc chipset
    if ( $band eq "bonly" )
    {
       $hostapd_conf .= "hw_mode=b\n";
    }
    elsif ( $band eq "bandg" )
    {
       $hostapd_conf .= "hw_mode=g\n";
    }
    elsif ( $band eq "gonly" )
    {
       $hostapd_conf .= "hw_mode=g\n";
       $hostapd_conf .= "basic_rates=60\n"; # Require 6 Mbps support - blocks 802.11b clients
    }
    else # ( $band eq "mixed" )
    {
       #mixed
       $hostapd_conf .= "hw_mode=g\n";
       $hostapd_conf .= "basic_rates=60\n"; # Require 6 Mbps support - blocks 802.11b clients

       # Add N protocol for mixed mode
       $hostapd_conf .= "ieee80211n=1\n";
       $hostapd_conf .= "wmm_enabled=1\n";

       my $capabilities = "[RX-STBC1][SHORT-GI-20][SHORT-GI-40]";
       #[HT40-] : 5-13
       #[HT40+] : 1-7
       if ( $channel < 6 )
       {
          $capabilities .= "[HT40+]";
       }
           else
       {
          $capabilities .= "[HT40-]";
       }

       $hostapd_conf .= "ht_capab=$capabilities\n";

    }

    $hostapd_conf .= "channel=$channel\n";
    $hostapd_conf .= "ignore_broadcast_ssid=$ssid_broadcast_bits\n";
    $hostapd_conf .= "macaddr_acl=0\n";
    $hostapd_conf .= "auth_algs=1\n";
    $hostapd_conf .= "wpa=$wpa_bits\n";

    $hostapd_conf .= "wpa_passphrase=$presharedkey\n";

    $hostapd_conf .= "wpa_key_mgmt=WPA-PSK\n";
    $hostapd_conf .= "wpa_pairwise=TKIP CCMP\n";
    $hostapd_conf .= "rsn_pairwise=CCMP\n";

    return $hostapd_conf;
}

sub write_hostapd_conf
{
    my ( $hostapd_text ) = @_;

    if( not defined $hostapd_text or $hostapd_text =~ /^\s*$/m )
    {
        print "ERROR: write_hostapd_conf called without valid argument for file contents\n";
        return;
    }

    open my $hostapd_conf_fh, '>', "$hostapd_conf_filename"
        or die "Couldn't open $hostapd_conf_filename for writing";

    print $hostapd_conf_fh $hostapd_text;

    close my $hostap_conf_fh;

    return 1;
}

sub check_ssid()
{

   #If the SSID was never set, fix it
   if ( &is_empty($config_data_ref->{'SSID'}) and $config_data_ref->{'ENABLE'} eq "y" )
   {
      my $modelno = $ENV{FC_MODELNO};
      chomp($modelno);

      my $wifi_ssid = $modelno;

      my $wifi_mac = `getmac $wifi_int 2>/dev/null`;
      chomp($wifi_mac);
      if ( $wifi_mac =~ /((?:[0-9a-f]{2}[:-]){5}[0-9a-f]{2})/i )
      {
         $wifi_mac = &clean_white($wifi_mac);
         $wifi_mac =~ s/://g;
         $wifi_mac_length = length($wifi_mac);
         $wifi_mac_end = uc(substr($wifi_mac, $wifi_mac_length-6, $wifi_mac_length));
         $wifi_ssid = "$modelno-$wifi_mac_end";
      }
      else
      {
         #If the hardware is missing, cannot set this up, so do not enable by default
         syslog("ERROR: Cannot determine MAC address from interface $wifi_int (missing hardware?)");
         exit(1);
      }

      my $serialno = $ENV{FC_SERIALNO};
      chomp($serialno);
      my $wifi_presharedkey = $serialno;
      if ( $serialno =~ /^.*\-(.*)$/ )
      {
         #if there is a dash in the serialno, use what is after the dash
         $wifi_presharedkey = $1;
      }
      chomp($wifi_presharedkey);

      #if it is too short, prepend it
      if ( length($wifi_presharedkey) < 8 )
      {
         $wifi_presharedkey = "redlion" . $wifi_presharedkey;
      }

      my $gau_opts = " --subsystem=wifi "
                   . " --enable=y "
                   . " --ssid=\"$wifi_ssid\" "
                   . " --presharedkey=\"$wifi_presharedkey\" "
                   . " --action=Save ";
      system("$gau_cmd $gau_opts &> /dev/null");

      #read it again, since we just set defaults, and the wifi.conf got written out again
      $config_data_ref = &read_jbm_conf_file( $conf_file );
   }
}

sub setup_dhcp_server()
{

    #Setup the dhcp server entries for these IPs
   syslog("Setting up DHCPD server");
    my $wifi_enable = $config_data_ref->{'ENABLE'};
    my $wifi_ipaddr = $config_data_ref->{'ADDRESS'};
    my $wifi_netmask = $config_data_ref->{'NETMASK'};

    if ( ! is_ip_address($wifi_ipaddr) || ! is_ip_address($wifi_netmask) )
    {
       syslog( "ERROR: Cannot reconfigure dhcpd server for Wifi, IP or Netmask invalid.\n");
       return 1;
    }

    #less than 16 avail IPs, don't get it up, 255.255.255.240
    my $tmp_cidrmask = mask2cidr($wifi_netmask);
    if ( $tmp_cidrmask > 28 ||  $tmp_cidrmask  < 24 )
    {
       syslog( "ERROR: Cannot reconfigure dhcpd serverfor Wifi, Netmask needs to be /24 to /28 (/$tmp_cidrmask).\n");
       return 1;
    }

    my $wifi_ipaddr_num = ipdot2num($wifi_ipaddr);
    my $wifi_subnet = get_network($wifi_ipaddr, $wifi_netmask );
    my $wifi_broadcast  = get_broadcast($wifi_ipaddr, $wifi_netmask );
    my $wifi_dns1 = $wifi_ipaddr;
    my $wifi_saddr;
    my $wifi_eaddr;
    my $wifi_saddr_num = ipdot2num($wifi_subnet) + 1;
    my $wifi_eaddr_num = ipdot2num($wifi_broadcast) - 1;

    if ( $wifi_ipaddr_num eq $wifi_saddr_num )
    {
       #if the interface IP is first IP, increment the start range
       $wifi_saddr_num++;
    }
    if ( $wifi_ipaddr_num eq $wifi_eaddr_num )
    {
       #If the interface is the last IP, decrement the last IP
       $wifi_eaddr_num--;
    }

    $wifi_saddr = ipnum2dot($wifi_saddr_num);
    $wifi_eaddr = ipnum2dot($wifi_eaddr_num);

    my %xml_settings = ();

    if ("y" eq $wifi_enable) {
        # Eth1 dhcpserver should never be enabled if wifi is enabled
        $xml_settings{"eth1_enable"} = "No";
        $xml_settings{"wifi_enable"} = "Yes";
    }
    else {
        if ($GAUWEB_SUBSYSTEM) {
            $xml_settings{"eth1_enable"} = "Yes";
        }
        $xml_settings{"wifi_enable"} = "No";
    }
    if ($GAUWEB_SUBSYSTEM) {
        #Setting up dhcpserver for eth1
        $xml_settings{"eth1_defaultgateway"} = $wifi_ipaddr;
        $xml_settings{"eth1_subnet"}         = $wifi_subnet;
        $xml_settings{"eth1_broadcast"}      = $wifi_broadcast;
        $xml_settings{"eth1_saddr"}          = $wifi_saddr;
        $xml_settings{"eth1_eaddr"}          = $wifi_eaddr;
    }
    #Setting up dhcpserver on WiFi
    $xml_settings{"wifi_defaultgateway"} = $wifi_ipaddr;
    $xml_settings{"wifi_subnet"}         = $wifi_subnet;
    $xml_settings{"wifi_broadcast"}      = $wifi_broadcast;
    $xml_settings{"wifi_saddr"}          = $wifi_saddr;
    $xml_settings{"wifi_eaddr"}          = $wifi_eaddr;
    $xml_settings{"wifi_netmask"}        = $wifi_netmask;
    $xml_settings{"dns1"}                = $wifi_ipaddr;
    &xfg_set_multi_attribute("dhcpserver", \%xml_settings);
    if ($GAUWEB_SUBSYSTEM) {
        # Clear settings map
        $xml_settings = ();
        #setting up eth1
        $xml_settings{"usedhcp"}             = "n";
        $xml_settings{"address"}             = $wifi_ipaddr;
        $xml_settings{"netmask"}             = $wifi_netmask;
        &xfg_set_multi_attribute("eth1", \%xml_settings);
    }
    $ret = &xfg_commit("saveall");

    if ( $ret )
    {
       syslog("Failed to setup dhcpd server IPs for WiFi");
       return 1;
    }

    return 0;
}

sub check_conf_md5()
{
   if ( -e "$conf_file_md5" )
   {
      system("md5sum -s -c $conf_file_md5 &> /dev/null");
      if ( 0 == $? )
      {
         #nothing to do, config file not changed
         syslog("$conf_file md5 unchanged");
         exit(0);
      }
   }

   system("md5sum $conf_file 1> $conf_file_md5 2>/dev/null")
   
}

sub check_wifi_config()
{

    check_conf_md5();

    #always write the config file data at least once to hostapd.
    #Might be started even if we are not enabled at boot time
    my $hostapd_conf_text = &generate_hostapd_conf( $config_data_ref );
    if( 0 < $DEBUG_LEVEL )
    {
        print "hostapd.conf contents:\n";
        print $hostapd_conf_text;
    }
    &syslog( "Writing hostapd.conf to $hostapd_conf_filename" );
    my $result  = &write_hostapd_conf( $hostapd_conf_text );

}
